#include "../include/antiAlias.h"
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
AntiAlias::AntiAlias()
{
    for(auto& f : filters)
    {
        f.setType(Biquad::LOWPASS_1POLE);
    }
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void AntiAlias::setCutoff(float cutOff)
{
    for(auto& f : filters)
    {
        f.initFilter(cutOff, 0.2);
    }
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void AntiAlias::process(std::vector<float>& inOutBuff)
{
    for(auto& f : filters)
    {
        f.process(inOutBuff);
    }   
}
